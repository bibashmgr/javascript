// While Loop:
// Syntax:
// while(condition){ // code to be executed }

let x = 0;
while (x < 10) {
    // console.log(x);
    x++;
}

// Do While Loop:
// Syntax:
// do{ // code to be executed }while(condition)

let y = 0;
do {
    console.log(y);
    y++;
} while (y < 0)

// even the condition isnot met,it will still execute the code